import type { McpServer } from "@modelcontextprotocol/sdk/server/mcp.js";
import { z } from "zod";
import type { AppConfig } from "../types.js";
import { F5XcClient, handleApiError } from "../services/f5-xc-client.js";
import { DEFAULT_PAGE_SIZE, MAX_PAGE_SIZE, CHARACTER_LIMIT } from "../constants.js";

// Customer Edge (CE) lifecycle:
//   - Registration token: POST /api/register/namespaces/system/tokens  (body: {metadata: {name, namespace:"system"}, spec:{}})
//   - Site list/status:   GET  /api/config/namespaces/system/sites     (read-only — CE registers itself, we only observe)
//   - Site delete:        DELETE /api/config/namespaces/system/sites/{name}
//   - Cloud CE deployment is primarily Terraform-driven via the volterraedge/volterra provider.
// Token create quirk: body must use {metadata: {name, namespace}, spec:{}} wrapper, not bare {name}.
//
// Terraform HCL generation for CE is done via this tool, not terraform-runner.ts,
// because CE config varies significantly by cloud provider.

const PaginationSchema = {
  page_start: z.number().int().min(0).default(0).describe("Pagination offset"),
  page_limit: z.number().int().min(1).max(MAX_PAGE_SIZE).default(DEFAULT_PAGE_SIZE).describe("Max items to return"),
};

const DryRunSchema = {
  dryRun: z.boolean().optional().describe("Preview the API call without executing it"),
};

function truncate(text: string): string {
  if (text.length <= CHARACTER_LIMIT) return text;
  return text.slice(0, CHARACTER_LIMIT) + `\n\n[Truncated. Use page_start/page_limit to paginate.]`;
}

function generateAzureHcl(params: {
  tenant: string;
  siteName: string;
  token: string;
  region: string;
  resourceGroup: string;
  vnetName: string;
  subnetName: string;
  siteLatitude: number;
  siteLongitude: number;
}): string {
  return `# F5 XC Customer Edge — Azure
# Generated by F5 XC MCP Server
# Provider: volterraedge/volterra
# Reference: https://registry.terraform.io/providers/volterraedge/volterra/latest/docs

terraform {
  required_providers {
    volterra = {
      source  = "volterraedge/volterra"
      version = ">= 0.11"
    }
    azurerm = {
      source  = "hashicorp/azurerm"
      version = ">= 3.0"
    }
  }
}

provider "volterra" {
  api_p12_file = var.api_p12_file
  url          = "https://${params.tenant}.console.ves.volterra.io/api"
}

provider "azurerm" {
  features {}
}

variable "api_p12_file" {
  description = "Path to F5 XC .p12 credentials file"
  type        = string
}

resource "volterra_azure_vnet_site" "${params.siteName.replace(/-/g, "_")}" {
  name      = "${params.siteName}"
  namespace = "system"

  azure_region  = "${params.region}"
  resource_group = "${params.resourceGroup}"

  vnet {
    existing_vnet {
      resource_group = "${params.resourceGroup}"
      vnet_name      = "${params.vnetName}"
    }
  }

  ingress_egress_gw {
    azure_certified_hw = "azure-byol-multi-nic-voltmesh"
    az_nodes {
      azure_az  = "1"
      disk_size = 80

      inside_subnet {
        subnet {
          subnet_resource_grp = "${params.resourceGroup}"
          vnet_resource_group = true
          subnet_name         = "${params.subnetName}-inside"
        }
      }
      outside_subnet {
        subnet {
          subnet_resource_grp = "${params.resourceGroup}"
          vnet_resource_group = true
          subnet_name         = "${params.subnetName}-outside"
        }
      }
    }
  }

  coordinates {
    latitude  = ${params.siteLatitude}
    longitude = ${params.siteLongitude}
  }

  registration_token = "${params.token}"
}

output "site_name" {
  value = volterra_azure_vnet_site.${params.siteName.replace(/-/g, "_")}.name
}
`;
}

